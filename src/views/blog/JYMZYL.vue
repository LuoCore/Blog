        <p>集运中的“集”，是 “集中”还是“集合”？大家可能都没有深究其意。通过查询相关资料，初步总结了一下，“集中”和“集合”在词义上是相近的，集中强调的是聚集的过程，集合强调的聚集的已知对象。集运的对象是未知的，主要是帮助客户把货物集中到一起，一起发往目的地；所以集运中的“集”，应该表达的是“集中”之意。</p>
    


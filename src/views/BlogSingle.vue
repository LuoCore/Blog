        <p>集运是近年来出现频率比较高的物流名词；是指货物集中在一起，再发往国外收件人的一种国际物流方式。现在大家在日常交流提到“集运”；或在网络搜索“集运”都会指向这种国际物流方式；已经成为大家约定俗成的一种说法。在集运之前，还出现过“转运”“代运”的说法，虽然在词义上不同，但是所指的这种国际物流方式是一样的。</p>
        <p>当前的集运是从转运的模式上发展起来的，转运一般指中国居民在国外电商平台或商场购买商品后，集中委托国外的物流公司运往中国。由此可见，转运和集运的运作模式是一样的，只是物流方向不一样；为了方便大家区分“集运”和“转运”，我们早期在实施开发集运系统项目，在转运前面冠以“海淘”，称之为“海淘转运”，在集运前面冠以“出口”，称之为“出口集运”，方便大家理解。在“集运”没有确立江湖地位之前，“代运”也是这种国际物流形式的另一种表达。这种表达差异与地域环境有关，在集运发展的初期，大部分东南亚的客户称之为“代运”；台湾地区的客户称之为“集运”。</p>
